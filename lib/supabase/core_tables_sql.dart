-- ── 13. messages (Lab Chat) ────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS messages (
    message_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message_user_uid       uuid  REFERENCES users(user_auth_uid) ON DELETE SET NULL,

    -- Channel / context
    message_channel      TEXT      NOT NULL DEFAULT 'general',
    -- general | strains | samples | fish | reagents | equipment | announcements
    message_context_type TEXT,     -- optional: pin message to a record type
    -- strain | sample | fishline | reagent | equipment | protocol
    message_context_id   BIGINT,   -- optional: FK resolved in app layer

    -- Content
    message_body         TEXT      NOT NULL,
    message_edited       BOOLEAN   DEFAULT false,
    message_edited_at    TIMESTAMP,

    -- Threading
    message_parent_id    BIGINT    REFERENCES messages(message_id) ON DELETE CASCADE,
    -- null = top-level, non-null = reply in thread

    -- Reactions / pinning (lightweight)
    message_pinned       BOOLEAN   DEFAULT false,
    message_pinned_by    BIGINT    REFERENCES users(user_id),

    -- Metadata
    message_created_at   TIMESTAMP DEFAULT NOW(),
    message_updated_at   TIMESTAMP DEFAULT NOW(),
    message_deleted      BOOLEAN   DEFAULT false  -- soft delete
);

-- ── 15. facility_sops ──────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS facility_sops (
    sop_id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Identity
    sop_code             TEXT,                      -- e.g. SOP-001, PROT-003
    sop_name             TEXT NOT NULL,
    sop_version          TEXT DEFAULT '1.0',
    sop_type             TEXT DEFAULT 'sop'
                         CHECK (sop_type IN ('sop','protocol','guideline','checklist','form','training')),
    sop_category         TEXT,                      -- Biosafety | Husbandry | Breeding | Health | Welfare …

    -- Status workflow
    sop_status           TEXT DEFAULT 'draft'
                         CHECK (sop_status IN ('draft','active','under_review','archived','superseded')),

    -- Content
    sop_description      TEXT,
    sop_tags             TEXT,                      -- comma-separated tags

    -- File storage (Supabase bucket: facility-sops)
    -- PDF slot (viewable in-app)
    sop_file_path        TEXT,                      -- storage path: {sop_id}/{filename}
    sop_file_name        TEXT,                      -- original filename
    sop_file_size        BIGINT,                    -- bytes
    sop_file_mime        TEXT,                      -- e.g. application/pdf
    -- TXT slot

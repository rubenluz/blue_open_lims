// core_tables_sql.dart - Complete PostgreSQL DDL for all core tables:
// users, samples, strains, fish_lines, fish_stocks, locations, machines,
// reagents, reservations, sops, messages, todo_items. Used by setup_page.

const String coreTablesSQL = '''

-- ═══════════════════════════════════════════════════════════════════════════════
-- Culture Collection Manager — Full Schema
-- Convention: every column prefixed with its table name
-- e.g. samples → sample_*   strains → strain_*   users → user_*
-- ═══════════════════════════════════════════════════════════════════════════════

-- ── Extensions ─────────────────────────────────────────────────────────────────
CREATE EXTENSION IF NOT EXISTS citext;

-- ── 1. app_meta ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS app_meta (
    meta_initialized   BOOLEAN   DEFAULT false,
    meta_version       TEXT      DEFAULT '1.0',
    meta_settings      JSONB     DEFAULT '{}'
);
-- Migrate existing installations: add meta_settings if not present
ALTER TABLE app_meta ADD COLUMN IF NOT EXISTS meta_settings JSONB DEFAULT '{}';

-- ── 2. users ───────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS users (
    user_id      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_auth_uid uuid UNIQUE,  -- for external auth providers (e.g. Supabase auth UID)

    user_email   CITEXT NOT NULL UNIQUE,
    user_name    TEXT,

    user_role    TEXT NOT NULL DEFAULT 'researcher'
                 CHECK (user_role IN ('superadmin', 'admin', 'technician', 'researcher', 'viewer')),

    user_status  TEXT DEFAULT 'pending'
                 CHECK (user_status IN ('pending','active','inactive')),

    user_phone   TEXT,

    user_orcid   TEXT,

    user_institution TEXT,
    user_group   TEXT,

    user_avatar_url TEXT,
    user_bio        TEXT,
    user_timezone   TEXT,
    user_language   TEXT,

    user_table_dashboard TEXT DEFAULT 'none'
        CHECK (user_table_dashboard IN ('read','write','none')),

    user_table_chat TEXT DEFAULT 'none'
        CHECK (user_table_chat IN ('read','write','none')),

    user_table_culture_collection TEXT DEFAULT 'none'
        CHECK (user_table_culture_collection IN ('read','write','none')),

    user_table_fish_facility TEXT DEFAULT 'none'
        CHECK (user_table_fish_facility IN ('read','write','none')),
    user_table_labels TEXT DEFAULT 'none'
        CHECK (user_table_labels IN ('read','write','none')),
    user_table_resources TEXT DEFAULT 'none'
        CHECK (user_table_resources IN ('read','write','none')),

    user_notifications_enabled BOOLEAN DEFAULT true,

    user_created_at TIMESTAMP DEFAULT NOW(),
    user_updated_at TIMESTAMP DEFAULT NOW(),
    user_last_login TIMESTAMP
);

-- ── 3. storage_locations ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS storage_locations (
    location_id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_name        TEXT    NOT NULL,
    location_type        TEXT,              -- fridge | freezer | incubator | shelf | cabinet | room
    location_room        TEXT,              -- physical room in the building
    location_temperature TEXT,             -- e.g. "4°C", "-80°C", "25°C"
    location_capacity    INT,              -- max number of items
    location_parent_id   BIGINT REFERENCES storage_locations(location_id),
    location_qrcode      TEXT,
    location_notes       TEXT,
    location_created_at  TIMESTAMP DEFAULT NOW()
);

-- ── 4. samples ─────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS samples (
    sample_id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Identifiers
    sample_code              TEXT     UNIQUE,
    sample_rebeca            TEXT,
    sample_ccpi              TEXT,
    sample_permit            TEXT,             
    sample_other_code        TEXT,

    -- Collection event
    sample_date              DATE,
    sample_collector         TEXT,
    sample_responsible       TEXT,

    -- Geography
    sample_country           TEXT,
    sample_archipelago       TEXT,
    sample_island            TEXT,
    sample_region            TEXT,
    sample_municipality      TEXT,
    sample_parish            TEXT,
    sample_local             TEXT,
    sample_gps               TEXT,
    sample_latitude          NUMERIC,
    sample_longitude         NUMERIC,
    sample_altitude_m        NUMERIC,

    -- Habitat
    sample_habitat_type      TEXT,
    sample_habitat_1         TEXT,
    sample_habitat_2         TEXT,
    sample_habitat_3         TEXT,
    sample_substrate         TEXT,
    sample_method            TEXT,

    -- Physical-chemical parameters
    sample_temperature       NUMERIC,         -- °C
    sample_ph                NUMERIC,
    sample_conductivity      NUMERIC,         -- µS/cm
    sample_oxygen            NUMERIC,         -- mg/L
    sample_salinity          NUMERIC,         -- ppt / PSU
    sample_radiation         NUMERIC,         -- µmol photons m⁻² s⁻¹
    sample_turbidity         NUMERIC,         -- NTU
    sample_depth_m           NUMERIC,

    -- Biological context
    sample_bloom             TEXT,
    sample_associated_organisms TEXT,

    -- Logistics
    sample_photos            TEXT,
    sample_preservation      TEXT,
    sample_transport_time_h  NUMERIC,

    -- Admin
    sample_project           TEXT,
    sample_observations      TEXT,
    sample_created_by        BIGINT REFERENCES users(user_id),
    sample_created_at        TIMESTAMP DEFAULT NOW(),
    sample_updated_at        TIMESTAMP DEFAULT NOW()
);


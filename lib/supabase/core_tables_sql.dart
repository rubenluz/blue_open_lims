    -- Identity & status
    strain_sample_code           TEXT REFERENCES samples(sample_code),  -- optional FK to samples(sample_code) for provenance
    strain_code                  TEXT UNIQUE,
    strain_status                TEXT,         -- alive | dead | lost | cryopreserved | transferred
    strain_origin                TEXT,
    strain_situation             TEXT,         -- in culture | deposited | lost | quarantine
    strain_toxins                TEXT,
    strain_public                TEXT,         -- yes | no | restricted
    strain_private_collection    TEXT,
    strain_type_strain           TEXT,         -- yes | no
    strain_last_checked          DATE,
    strain_biosafety_level       TEXT,         -- BSL-1 | BSL-2 | BSL-3
    strain_access_conditions     TEXT,
    strain_other_codes           TEXT,

    -- Taxonomy
    strain_empire                TEXT,         -- Bacteria | Archaea | Eukaryota | Virus
    strain_kingdom               TEXT,
    strain_phylum                TEXT,
    strain_class                 TEXT,
    strain_order                 TEXT,
    strain_family                TEXT,
    strain_genus                 TEXT,
    strain_species               TEXT,
    strain_subspecies            TEXT,
    strain_variety               TEXT,
    strain_scientific_name       TEXT,
    strain_authority             TEXT,
    strain_other_names           TEXT,
    strain_taxonomist            TEXT,
    strain_identification_method TEXT,         -- morphology | 16S | 18S | ITS | genome | combined
    strain_identification_date   DATE,

    -- Morphology
    strain_morphology            TEXT,         -- unicellular | filamentous | colonial | coccoid
    strain_cell_shape            TEXT,
    strain_cell_size_um          TEXT,
    strain_motility              TEXT,         -- motile | non-motile | gliding
    strain_pigments              TEXT,
    strain_colonial_morphology   TEXT,

    -- Photos
    strain_photo                 TEXT,
    strain_public_photo          TEXT,
    strain_microscopy_photo      TEXT,

    -- Herbarium deposition
    strain_herbarium_code        TEXT,
    strain_herbarium_name        TEXT,         -- e.g. Ruy Telles Palhinha Herbarium
    strain_herbarium_status      TEXT,         -- present | missing | on loan
    strain_herbarium_date        DATE,
    strain_herbarium_method      TEXT,
    strain_herbarium_notes       TEXT,

    -- Culture maintenance
    strain_last_transfer         DATE,
    strain_periodicity           INT,          -- transfer interval in days
    strain_next_transfer         DATE,
    strain_medium                TEXT,         -- BG11 | f/2 | LB | …
    strain_medium_salinity       TEXT,         -- freshwater | marine | brackish
    strain_light_cycle           TEXT,         -- e.g. "12:12 L:D"
    strain_light_intensity_umol  NUMERIC,      -- µmol photons m⁻² s⁻¹
    strain_temperature_c         NUMERIC,      -- incubation °C
    strain_co2_pct               NUMERIC,
    strain_aeration              TEXT,         -- still | shaken | bubbled | perfused
    strain_culture_vessel        TEXT,         -- flask | plate | tube | bioreactor
    strain_room                  TEXT,
    strain_location_id           BIGINT REFERENCES storage_locations(location_id),
    strain_position_in_location  TEXT,

    -- Cryopreservation
    strain_cryo_date             DATE,
    strain_cryo_method           TEXT,         -- glycerol | DMSO | liquid nitrogen | lyophilisation
    strain_cryo_location         TEXT,
    strain_cryo_vials            INT,
    strain_cryo_responsible      TEXT,
    strain_cryo_responsible_id   BIGINT REFERENCES users(user_id),

    -- Responsible maintainer
    strain_responsible_id        BIGINT REFERENCES users(user_id),

    -- Isolation
    strain_isolation_responsible TEXT,
    strain_isolation_responsible_id BIGINT REFERENCES users(user_id),
    strain_isolation_date        DATE,
    strain_isolation_method      TEXT,         -- single cell | serial dilution | plating
    strain_deposit_date          DATE,

    -- Molecular — prokaryotes
    strain_seq_16s_bp            INT,
    strain_its                   TEXT,
    strain_its_bands             TEXT,
    strain_cloned_gel            TEXT,
    strain_genbank_16s_its       TEXT,
    strain_genbank_status        TEXT,         -- submitted | published | pending | none
    strain_genome_pct            NUMERIC,
    strain_genome_cont           INT,
    strain_genome_16s            TEXT,
    strain_gca_accession         TEXT,

    -- Molecular — eukaryotes
    strain_seq_18s_bp            INT,
    strain_genbank_18s           TEXT,
    strain_its2_bp               INT,
    strain_genbank_its2          TEXT,
    strain_rbcl_bp               INT,
    strain_genbank_rbcl          TEXT,
    strain_tufa_bp               INT,          -- tufA (green algae)
    strain_genbank_tufa          TEXT,
    strain_cox1_bp               INT,          -- COX1 (some protists / algae)
    strain_genbank_cox1          TEXT,

    -- Bioactivity & applications
    strain_bioactivity           TEXT,
    strain_metabolites           TEXT,
    strain_industrial_use        TEXT,
    strain_growth_rate           TEXT,

    -- References
    strain_publications          TEXT,
    strain_external_links        TEXT,
    strain_notes                 TEXT,
    strain_qrcode                TEXT,

    strain_created_at            TIMESTAMP DEFAULT NOW(),
    strain_updated_at            TIMESTAMP DEFAULT NOW()
);


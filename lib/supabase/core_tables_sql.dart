    requested_strain_requester_email        TEXT,
    requested_strain_requester_institution  TEXT,
    requested_strain_requester_country      TEXT,
    requested_strain_requester_group        TEXT,

    -- Purpose
    requested_strain_purpose                TEXT,    -- research | teaching | commercial
    requested_strain_project_title          TEXT,
    requested_strain_funding_source         TEXT,
    requested_strain_publication            TEXT,

    -- Legal / compliance
    requested_strain_mta_required           BOOLEAN,
    requested_strain_mta_sent_date          DATE,
    requested_strain_mta_signed_date        DATE,
